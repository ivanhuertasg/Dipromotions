import { useState, useEffect, useCallback } from 'react';

const STORAGE_KEY = 'dipromotions_language';

export function useI18n() {
  const [currentLang, setCurrentLangState] = useState<Language>(() => {
    if (typeof window !== 'undefined') {
      const stored = localStorage.getItem(STORAGE_KEY) as Language;
      if (stored && translations[stored]) {
        return stored;
      }
    }
    return defaultLanguage;
  });

  const setLanguage = useCallback((lang: Language) => {
    setCurrentLangState(lang);
    if (typeof window !== 'undefined') {
      localStorage.setItem(STORAGE_KEY, lang);
      document.documentElement.lang = lang;
    }
  }, []);

  // Inicializar lang del documento
  useEffect(() => {
    document.documentElement.lang = currentLang;
  }, [currentLang]);

  // Funci√≥n de traducci√≥n
  const t = useCallback(
    (key: string, params?: Record<string, string | number>): string => {
      const keys = key.split('.');
      let value: unknown = translations[currentLang];

      for (const k of keys) {
        if (value && typeof value === 'object' && k in value) {
          value = (value as Record<string, unknown>)[k];
        } else {
          // Fallback al espa√±ol
          value = translations[defaultLanguage];
          for (const fk of keys) {
            if (value && typeof value === 'object' && fk in value) {
              value = (value as Record<string, unknown>)[fk];
            } else {
              return key; // Retornar la clave si no se encuentra
            }
          }
          break;
        }
      }

      if (typeof value === 'string') {
        // Reemplazar par√°metros
        if (params) {
          return Object.entries(params).reduce((acc, [paramKey, paramValue]) => {
            return acc.replace(new RegExp(`{${paramKey}}`, 'g'), String(paramValue));
          }, value);
        }
        return value;
      }

      return key;
    },
    [currentLang]
  );

  return {
    currentLang,
    setLanguage,
    t,
    supportedLanguages,
  };
}

export default useI18n;
